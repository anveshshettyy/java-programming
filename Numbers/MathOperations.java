package Numbers;

public class MathOperations {
    public static void main(String args[]) {
       int a = 10;
       int b = 5;
       
       int sum = a + b;
       int prod = a * b;
       int div = a / b;

       System.out.println("Sum is: "+sum);
       System.out.println("Product is: "+prod);
       System.out.println("Quotient is: "+div);
    }
}
